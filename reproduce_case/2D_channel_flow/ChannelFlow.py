# 全域体力驱动 + 左右周期边界 + 上下反弹边界（通道流）
# 使用 D2Q9 格子玻尔兹曼方法（LBM）求解二维通道流
# ------------------------------------------------------------
# 物理含义：
# - 全域体力驱动：在 x 方向对整个计算域施加一个很小的恒定体力 Fx，使流体在各处同时被加速，
#   不同于传统由入口/出口压差驱动的流动，因此不会形成明显的从左到右推进过程
# - 左右边界（周期边界）：通过 np.roll 实现周期性连接，流体从一侧流出会从另一侧进入，消除了 x 方向上的空间不对称性
# - 上下边界（无滑移壁面）：使用 bounce-back（反弹）方法，让流体在碰到上下边界时“弹回去”，
#   从而使边界处的流体速度为 0，相当于贴着静止的墙面不滑动
# - 保存每隔若干步的速度场 ux，用于后处理或作图
# ------------------------------------------------------------

import numpy as np
import os

# =========================
# 0. 输出目录
# =========================
# 用于保存每隔一段时间输出的速度场数据
output_dir = "lbm_output"
os.makedirs(output_dir, exist_ok=True)
# =========================
# 1. 参数设置
# =========================
# 计算域尺寸
# nx: x方向格点数（通道长度）
# ny: y方向格点数（通道高度）
nx, ny = 120, 50

# 松弛时间 tau
# 控制流体粘性，LBM中运动粘性满足：
# nu = (tau - 0.5) / 3
tau = 0.8

# 松弛频率 omega = 1 / tau
# 在BGK碰撞模型中会用到
omega = 1.0 / tau

# x方向体力
# 用一个很小的常数体力驱动流动，相当于施加压力梯度
Fx = 1e-6

# 总迭代步数
total_steps = 12000

# 每隔多少步保存一次速度场
save_interval = 100

# =========================
# 2. D2Q9 离散速度模型
# =========================
# D2Q9 = 2维，9个离散速度方向
#
# 编号通常可理解为：
#   6 2 5
#   3 0 1
#   7 4 8
#
# 其中：
# 0  : 静止
# 1  : 向右
# 2  : 向上
# 3  : 向左
# 4  : 向下
# 5~8: 四个对角方向

# 每个方向的 x 分量
cx = np.array([0, 1, 0, -1, 0, 1, -1, -1, 1])

# 每个方向的 y 分量
cy = np.array([0, 0, 1, 0, -1, 1, 1, -1, -1])

# D2Q9 的权重系数
# 平衡分布函数 feq 中会用到
w = np.array([
    4/9,                # 静止方向
    1/9, 1/9, 1/9, 1/9, # 四个正交方向
    1/36, 1/36, 1/36, 1/36  # 四个对角方向
])

# 对应反方向索引
# 比如：
# 1(右) <-> 3(左)
# 2(上) <-> 4(下)
# 5 <-> 7
# 6 <-> 8
# 以后如果做一般障碍物 bounce-back 会很方便
opp = np.array([0, 3, 4, 1, 2, 7, 8, 5, 6])

# =========================
# 3. 初始化宏观量
# =========================

# 初始密度场，全部设为1
rho = np.ones((ny, nx))

# 初始速度场，设为0
ux = np.zeros((ny, nx))
uy = np.zeros((ny, nx))

# =========================
# 4. 平衡分布函数
# =========================
def equilibrium(rho, ux, uy):
    """
    计算 D2Q9 的平衡分布函数 feq

    参数：
        rho : 密度场，形状 (ny, nx)
        ux  : x方向速度场，形状 (ny, nx)
        uy  : y方向速度场，形状 (ny, nx)

    返回：
        feq : 平衡分布函数，形状 (9, ny, nx)

    数学形式：
        feq_k = w_k * rho * [1 + 3(e_k·u) + 9/2(e_k·u)^2 - 3/2|u|^2]
    """
    feq = np.zeros((9, ny, nx))

    # 速度平方 |u|^2 = ux^2 + uy^2
    u2 = ux**2 + uy**2

    # 对9个离散方向分别计算平衡分布
    for k in range(9):
        # cu = 3 * (e_k · u)
        # e_k = (cx[k], cy[k])
        cu = 3.0 * (cx[k] * ux + cy[k] * uy)

        # D2Q9 的标准二阶平衡分布
        feq[k] = w[k] * rho * (1.0 + cu + 0.5 * cu**2 - 1.5 * u2)

    return feq

# 初始分布函数直接设为平衡态
# 即：一开始整个流场静止、均匀
f = equilibrium(rho, ux, uy)

# =========================
# 5. 单步推进函数
# =========================
def lbm_step(f):
    """
    执行一步 LBM 更新：
    1) 由分布函数恢复宏观量 rho, ux, uy
    2) 加入体力修正
    3) 碰撞（BGK）
    4) 推进（streaming）
    5) 上下壁面施加 bounce-back 边界条件

    参数：
        f : 当前时刻分布函数，形状 (9, ny, nx)

    返回：
        f_stream : 下一时刻分布函数
    """

    # -------------------------
    # (1) 恢复宏观量
    # -------------------------
    # 密度 rho = sum_k f_k
    rho = np.sum(f, axis=0)

    # 速度由动量矩恢复：
    # ux = sum_k(f_k * cx_k) / rho
    # uy = sum_k(f_k * cy_k) / rho
    ux = np.sum(f * cx[:, None, None], axis=0) / rho
    uy = np.sum(f * cy[:, None, None], axis=0) / rho

    # -------------------------
    # (2) 体力修正
    # -------------------------
    # Guo forcing 常见写法中，速度常加半步力修正：
    # u <- u + F / (2*rho)
    # 这里只在 x 方向施加体力 Fx
    ux += Fx / (2.0 * rho)

    # -------------------------
    # (3) 计算平衡态
    # -------------------------
    feq = equilibrium(rho, ux, uy)

    # -------------------------
    # (4) 构造体力项 F_k
    # -------------------------
    # 这里使用 Guo forcing 形式的一部分
    # 作用是把连续介质中的体力项映射到离散 Boltzmann 方程中
    F = np.zeros_like(f)

    for k in range(9):
        # eu = e_k · u
        eu = cx[k] * ux + cy[k] * uy

        # 这里只有 x 方向体力 Fx，因此公式只写与 Fx 对应的部分
        # 一般二维完整形式还可写成 Fx 和 Fy 的组合
        F[k] = w[k] * (1.0 - 0.5 * omega) * (
            (3.0 * (cx[k] - ux) + 9.0 * eu * cx[k]) * Fx
        )

    # -------------------------
    # (5) 碰撞步骤
    # -------------------------
    # BGK 碰撞：
    # f* = f - omega * (f - feq) + F
    #
    # 含义：
    # - 分布函数向平衡分布弛豫
    # - 同时加入体力项
    f = f - omega * (f - feq) + F

    # -------------------------
    # (6) 推进步骤
    # -------------------------
    # 沿各自离散速度方向搬运分布函数
    #
    # np.roll 的意义：
    # - axis=1 对应 x 方向
    # - axis=0 对应 y 方向
    #
    # 由于使用了 np.roll，左右和上下默认都是“周期搬运”
    # 但后面我们会手动覆盖上下边界，让它们变成 bounce-back
    f_stream = np.empty_like(f)

    for k in range(9):
        f_stream[k] = np.roll(
            np.roll(f[k], shift=cx[k], axis=1),  # x方向移动
            shift=cy[k], axis=0                  # y方向移动
        )

    # -------------------------
    # (7) 上壁面 / 下壁面 bounce-back
    # -------------------------
    # 这里的通道是 y方向有上下两堵墙
    # 顶部 y=0，底部 y=ny-1
    #
    # bounce-back 的物理意义：
    # 撞到壁面的粒子反向返回，等价于无滑移边界（壁面速度为0）
    #
    # 注意：
    # 由于先做了 streaming，这里需要把壁面处“指向流体内部”的未知分布
    # 用碰撞后、推进前的相反方向分布替代

    # ---- 顶壁 y = 0 ----
    # 指向下方流体内部的分布：2, 5, 6
    # 分别来自相反方向：4, 7, 8
    f_stream[2, 0, :] = f[4, 0, :]
    f_stream[5, 0, :] = f[7, 0, :]
    f_stream[6, 0, :] = f[8, 0, :]

    # ---- 底壁 y = ny-1 ----
    # 指向上方流体内部的分布：4, 7, 8
    # 分别来自相反方向：2, 5, 6
    f_stream[4, -1, :] = f[2, -1, :]
    f_stream[7, -1, :] = f[5, -1, :]
    f_stream[8, -1, :] = f[6, -1, :]

    # 左右方向没有单独处理，因此保持周期边界
    return f_stream

# =========================
# 6. 主循环
# =========================
# 迭代推进流场，并周期性保存 ux 数据
step = 0

for step in range(total_steps):
    # 执行一步 LBM
    f = lbm_step(f)

    # 每隔 save_interval 步保存一次 x方向速度
    if step % save_interval == 0:
        # 从分布函数恢复密度
        rho = np.sum(f, axis=0)

        # 恢复 x方向速度
        ux = np.sum(f * cx[:, None, None], axis=0) / rho

        # 保存为 .npy 文件，便于后续用 numpy 直接读取
        filename = os.path.join(output_dir, f"ux_{step:06d}.npy")
        np.save(filename, ux)

        print(f"Saved step {step}")